"""

Задача 4. Тернарный оператор с регулярными выражениями

Условие:
Напишите программу, которая проверяет email на валидность используя тернарный оператор и regex.

Логика:

    Если email содержит '@' и '.' -> "Валидный email"
    Иначе -> "Невалидный email"

Пример входных данных:
    Email: "user@example.com"
"""

### ваше решение здесь ###
import re
email = 'user@example.com'
email_pattern = r'\w*@\w*\.\w*'
is_valid = 'Валидный email' if re.match(email_pattern, email) else 'Невалидный email'
print(is_valid)
"""

Задача 5. Сложная проверка с регулярными выражениями

Условие:
Напишите программу, которая анализирует сложность пароля используя тернарный оператор и regex.

Логика:

    Если пароль содержит цифры И 
    буквы в верхнем и нижнем регистре И длиной >= 8 -> "Надежный пароль"
    Иначе если пароль содержит цифры ИЛИ буквы в разных регистрах -> 
    "Средний пароль"  
    Иначе -> "Слабый пароль"

Пример входных данных:
    Пароль: "Secure123"
"""

### ваше решение здесь ###
password = 'Secure123'
has_uppercase = re.findall(r'[A-Z]', password)
has_lowercase = re.findall(r'[a-z]', password)
has_digit = re.findall(r'[0-9]', password)
is_safe = 'Надежный пароль' if has_digit and has_lowercase and has_uppercase \
    and len(password) >= 8  else 'Средний пароль' if has_digit or (has_lowercase and has_uppercase) \
    else 'Слабый пароль'
print(is_safe)
